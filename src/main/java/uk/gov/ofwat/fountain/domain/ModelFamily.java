package uk.gov.ofwat.fountain.domain;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

/**
 * a collection of themically related models, for example the JR family
 * will include the JR and all the JR-ICS's.
 * 
 * A family will have a single parent model and all reports based on any of the JR
 * family will be executed in the context of this model.
 * 
 * Membership of a family is optional. Other models will be stand-alone and will
 * execute reports in their own context
 */
@XmlRootElement(name = "modelFamily")
@XmlType(propOrder = { "id", "name" , "code"})
public class ModelFamily implements Entity, Coded, Serializable {
	
	private int id;
	private String name;
	private String code;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((code == null) ? 0 : code.hashCode());
		result = prime * result + id;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ModelFamily other = (ModelFamily) obj;
		if (code == null) {
			if (other.code != null)
				return false;
		} else if (!code.equals(other.code))
			return false;
		if (id != other.id)
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "ModelFamily [id=" + id + ", name=" + name + ", code=" + code
				+ "]";
	}

	
}
